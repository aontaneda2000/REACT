// => Variables: Son espacios de memoria que nos permiten almacenar datos
/* Existen dos limitaciones de nombre de variables en JavaScript:
  - El nombre únicamente puede incluir letras, dígitos, o los símbolos $ y _.
  - El primer carácter no puede ser un dígito. */
// => Las variables y funciones deben usar la notación camelCase. Es decir, la primera palabra debe comenzar con minúscula y las siguientes palabras deben comenzar con mayúscula.

var nombre; // => Crear una variable en JavaScript se denomina "declarar" una variable.

nombre = "Jesús"; // => Cuando asignamos un valor a una variable se denomina "inicializar" una variable.

nombre = "Daniel"; // => Cuando reasignamos o modificamos el valor de una variable se denomina "reasignar" una variable.

var nombre = "Brayan"; // => Con var podemos volver a declarar una variable.

console.log(nombre);

var apellido = "Chavez"; // declaracion e inicializacion de variable

var apellido = "Perez";
var edad = 20;
var correo = "correo@correo.com";

console.log(apellido);
console.log(edad);
console.log(correo);
